/**
 * Generates apps/web/src/lib/server/acroform/fonts/bundled-font-data.ts from the
 * pinned @fontsource/tajawal WOFF2 files. The output embeds the approved Arabic
 * and Latin font bytes as base64 data URLs so the serverless renderer does not
 * depend on runtime node_modules resolution, external URLs, or system fonts.
 *
 * Run this script whenever the pinned font packages or selected files change:
 *   node scripts/generate-bundled-font-data.mjs
 */

import fs from "node:fs/promises";
import fsSync from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT_DIR = path.join(ROOT, "src", "lib", "server", "acroform", "fonts");
const OUT_FILE = path.join(OUT_DIR, "bundled-font-data.ts");

const FONT_FILES = [
  { exportName: "TAJAWAL_ARABIC_400_WOFF2", file: "tajawal-arabic-400-normal.woff2" },
  { exportName: "TAJAWAL_ARABIC_700_WOFF2", file: "tajawal-arabic-700-normal.woff2" },
  { exportName: "TAJAWAL_LATIN_400_WOFF2", file: "tajawal-latin-400-normal.woff2" },
  { exportName: "TAJAWAL_LATIN_700_WOFF2", file: "tajawal-latin-700-normal.woff2" },
];

const FONTSOURCE_CANDIDATES = [
  path.join(ROOT, "node_modules", "@fontsource", "tajawal", "files"),
  path.join(ROOT, "..", "..", "node_modules", "@fontsource", "tajawal", "files"),
  path.join(ROOT, "..", "node_modules", "@fontsource", "tajawal", "files"),
];

async function main() {
  await fs.mkdir(OUT_DIR, { recursive: true });

  const lines = [
    "/**",
    " * Bundled WathiqCare overlay font bytes.",
    " *",
    " * This file is generated by scripts/generate-bundled-font-data.mjs from the",
    " * pinned @fontsource/tajawal package. Do not edit by hand.",
    " */",
    "",
  ];

  const fontsourcedir = FONTSOURCE_CANDIDATES.find((candidate) =>
    FONT_FILES.every(({ file }) => {
      try {
        return fsSync.statSync(path.join(candidate, file)).isFile();
      } catch {
        return false;
      }
    }),
  );
  if (!fontsourcedir) {
    throw new Error("Could not locate @fontsource/tajawal files in any candidate directory");
  }

  for (const { exportName, file } of FONT_FILES) {
    const sourcePath = path.join(fontsourcedir, file);
    const bytes = await fs.readFile(sourcePath);
    const base64 = Buffer.from(bytes).toString("base64");
    lines.push(`export const ${exportName} = "data:font/woff2;base64,${base64}";`);
    lines.push(`export const ${exportName}_BYTE_LENGTH = ${bytes.length};`);
    lines.push(`export const ${exportName}_SHA256 = "${await sha256Hex(bytes)}";`);
    lines.push("");
  }

  await fs.writeFile(OUT_FILE, lines.join("\n"), "utf8");
  console.log(`Wrote ${OUT_FILE}`);
}

async function sha256Hex(data) {
  const { createHash } = await import("node:crypto");
  return createHash("sha256").update(Buffer.from(data)).digest("hex");
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
